package com.douguo.dc.model;

public class DevicesModel {
	private String	model;
	private String	user;
	private String	userPer;
	private String	launch;
	private String	launchPer;

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getUserPer() {
		return userPer;
	}

	public void setUserPer(String userPer) {
		this.userPer = userPer;
	}

	public String getLaunch() {
		return launch;
	}

	public void setLaunch(String launch) {
		this.launch = launch;
	}

	public String getLaunchPer() {
		return launchPer;
	}

	public void setLaunchPer(String launchPer) {
		this.launchPer = launchPer;
	}
}
